export const CLASS_OPTIONS = (() => {
  const grades = ["10", "11", "12"];
  const majors = ["TKJ", "Ph", "OTKP", "TBSM", "MIA", "MIPA"]; //section kelas udh ga kepake skip
  const groups = [1, 2, 3];
  const list: string[] = [];
  grades.forEach((g) => {
    majors.forEach((m) => {
      groups.forEach((n) => {
        list.push(`${g} ${m} ${n}`);
      });
    });
  });
  return list;
})();

